package com.Object;
/**
 * program to use encapsulation
 * @author mkanakkanadar
 * 27/04/2023
 */
public class EncapsulationExample {
	 private int theatreID;
	 private String theatreName;
	 private int theatreTotalScreen;
	 private String theatreLocation;
	 
	 public int getTheatreID() {
		return theatreID;
	}

	public void setTheatreID(int theatreID) {
		this.theatreID = theatreID;
	}

	public String getTheatreName() {
		return theatreName;
	}

	public void setTheatreName(String theatreName) {
		this.theatreName = theatreName;
	}

	public int getTheatreTotalScreen() {
		return theatreTotalScreen;
	}

	public void setTheatreTotalScreen(int theatreTotalScreen) {
		this.theatreTotalScreen = theatreTotalScreen;
	}

	public String getTheatreLocation() {
		return theatreLocation;
	}

	public void setTheatreLocation(String theatreLocation) {
		this.theatreLocation = theatreLocation;
	}

	
	

}
